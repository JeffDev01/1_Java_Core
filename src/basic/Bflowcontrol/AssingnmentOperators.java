package basic.Bflowcontrol;

public class AssingnmentOperators {
    public static void main(String[] args) {

        //=, += , *= , /= , %=

        int salary=1000;

        salary += 1000; //salary = salary + 1000
        salary *= 0.1;

        System.out.println(salary);

        int number = 22;

        number %=10; //remainder
        System.out.println(number);
    }

}
