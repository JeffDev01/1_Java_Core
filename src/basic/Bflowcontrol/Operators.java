package basic.Bflowcontrol;

public class Operators {

    public static void main(String[] args) {
        double numero1 = 10;
        double numero2 = 20;
        System.out.println(numero1 / numero2);


        //Remainder shows the left over after some computation
        double number1 = 22d;
        float number2 = 10f;
        System.out.println("The division's remainder of the computation is: "+ (number1%number2));
    }

}
