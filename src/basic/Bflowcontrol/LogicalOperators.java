package basic.Bflowcontrol;

public class LogicalOperators {
    public static void main(String[] agrs) {
        int age = 20;
        float salary = 2500.0f;

        System.out.println(age > 21 && salary < 2600);
        System.out.println(age > 21 || salary < 2600);
        System.out.println(age > 21 && salary < 2600);


    }

}
